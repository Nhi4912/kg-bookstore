const variant = (
	id: string,
	price: number,
	attrs: { name: string; value: string }[] = [],
) => ({
	id,
	product_id: "",
	image: null,
	price,
	sku: Math.floor(Math.random() * 900000 + 100000),
	barcode: "",
	retail_price: price,
	cost_price: Math.round(price * 0.6),
	stock_quantity: Math.floor(Math.random() * 100 + 5),
	created_at: "2024-01-01T00:00:00Z",
	updated_at: "2024-01-01T00:00:00Z",
	attributes: attrs.map((a, i) => ({
		id: `attr-${id}-${i}`,
		name: a.name,
		description: "",
		value: a.value,
		created_at: "2024-01-01T00:00:00Z",
		updated_at: "2024-01-01T00:00:00Z",
	})),
});

const v = (id: string) => MOCK_VENDORS.find((x) => x.id === id)!;

